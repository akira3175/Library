    public void loadSanPham() {
        SanPhamBUS spBUS = new SanPhamBUS();
        List<SanPhamDTO> spList = new ArrayList<>();
        spList = spBUS.layDanhSachTatCaSanPham();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (SanPhamDTO i : spList) {
            Object[] row = new Object[]{
                i.getMaSanPham(),
                i.getTenSanPham(),
                i.getSoLuong(),
                i.getGiaVon()};
            model.addRow(row);
        }
        jTable1.setModel(model);
    }

    public void loadNhaCungCap() {
        NhaCungCapBUS ncc = new NhaCungCapBUS();
        List<NhaCungCapDTO> nccList = new ArrayList<>();
        nccList = ncc.layTatCaNhaCungCap();

        for (NhaCungCapDTO i : nccList) {
            jComboBox1.addItem(i.getTenNhaCungCap());
        }
    }

    public void loadSanPhamDuocChon() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int tong = 0;

        model.setRowCount(0);

        for (SanPhamDTO i : spDuocChonList) {
            tong = tong + i.getGiaVon() * i.getSoLuong();
            Object[] row = new Object[]{
                i.getMaSanPham(),
                i.getTenSanPham(),
                i.getGiaVon(),
                i.getSoLuong()};
            model.addRow(row);
        }
        DecimalFormat formatter = new DecimalFormat("#,###");
        String formattedAmount = formatter.format(tong) + "đ";
        jLabel3.setText(formattedAmount);

        jTable2.setModel(model);
    }

    public boolean kiemTraSoLuong(String input) {
        if (Pattern.matches("^[1-9]\\d*$", input)) {
            return true;
        } else {
            return false;
        }
    }

    public boolean kiemTraSanPham(int MaSanPham, int soLuong) {
        for (SanPhamDTO i : spDuocChonList) {
            if (MaSanPham == i.getMaSanPham()) {
                int tam = i.getSoLuong();
                i.setSoLuong(tam + soLuong);
                return true;
            }
        }
        return false;
    }

